        private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
		// TODO add your handling code here:
		String writeContent = newsTextArea1.getText();
		if (writeContent.equals("")) {
			return;
		}
		SimpleDateFormat sdf = new SimpleDateFormat("yyyyMMdd HH.mm.ss.SSS", Locale.KOREAN);
		String strDate = sdf.format(new Date());
		String fileName = "";
		fileName = userHome + File.separator + "documents" + File.separator + strDate + "_Jsoup.parse.html";
		FileUtil.fileWrite(fileName, Jsoup.parse(writeContent).html());
		fileName = userHome + File.separator + "documents" + File.separator + strDate + "_as.is.html";
		FileUtil.fileWrite(fileName, writeContent);
		//showMessageDialog(Component parentComponent, Object message, String title, int messageType)
		JOptionPane.showMessageDialog(this, "파일을 저장하였습니다.", "알림", JOptionPane.INFORMATION_MESSAGE);
        }//GEN-LAST:event_jButton3ActionPerformed

