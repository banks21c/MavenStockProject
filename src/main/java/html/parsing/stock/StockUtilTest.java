	@Test
	public void testGetStockCodeFromNameList() {
		String SAMSUNG_CORP_NM[] = { "삼성전자", "삼성SDI", "삼성물산", "삼성전기", "삼성에스디에스", "삼성화재", "삼성생명", "호텔신라", "삼성증권",
				"삼성중공업", "에스원", "삼성엔지니어링", "제일기획", "신세계", "신세계인터내셔날", "신세계 I&C", "신세계푸드" };
		List<String> SAMSUNG_CORP_CD = new ArrayList<String>();
		String STR_SAMSUNG_CORP_CD[] = new String[SAMSUNG_CORP_NM.length];
		int idx = 0;
		for (String stockName : SAMSUNG_CORP_NM) {
			String stockCode = StockUtil.getStockCodeFromName(stockName);
			if (!stockCode.equals("")) {
				logger.debug(stockName + "'s stockCode is " + stockCode);
				SAMSUNG_CORP_CD.add(stockCode);
			} else {
				logger.debug(stockName + "'s stockCode is null");
			}
			idx++;
		}
		logger.debug("SAMSUNG_CORP_CD:" + SAMSUNG_CORP_CD);
		logger.debug("SAMSUNG_CORP_NM.length:" + SAMSUNG_CORP_NM.length);
		logger.debug("SAMSUNG_CORP_CD.size:" + SAMSUNG_CORP_CD.size());

		STR_SAMSUNG_CORP_CD = new String[SAMSUNG_CORP_CD.size()];
		logger.debug("STR_SAMSUNG_CORP_CD:" + STR_SAMSUNG_CORP_CD);

		STR_SAMSUNG_CORP_CD = SAMSUNG_CORP_CD.toArray(STR_SAMSUNG_CORP_CD);
		logger.debug("STR_SAMSUNG_CORP_CD:" + STR_SAMSUNG_CORP_CD);
		System.out.println(Arrays.toString(STR_SAMSUNG_CORP_CD));

		StringBuffer sb = new StringBuffer();
		int index = 0;
		for(String s :STR_SAMSUNG_CORP_CD) {
			if(index != 0) {
				sb.append(",");
			}
			sb.append("\""+s+"\"");
			index++;
		}
		logger.debug("sb :"+sb.toString());
	}

